package com.zukun.java;

public class BitWiseOperator {
	public static void main(String[] args) {
		int a=10;
		int b = 2;
		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(a^b);
		System.out.println(a<<b);// 10<<2 -> 40
		System.out.println(a>>b);// 2

		}


}
